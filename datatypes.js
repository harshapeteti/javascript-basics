let numberVar = 42;
let stringVar = 'Hello';
let booleanVar = true;
let undefinedVar;
let nullVar = null;
let objectVar = { key: 'value' };
let arrayVar = [1, 2, 3];
let functionVar = function() {
  console.log('Hello from a function!');
};
let symbolVar = Symbol('unique');

console.log(numberVar);     // Output: 42
console.log(stringVar);     // Output: 'Hello'
console.log(booleanVar);    // Output: true
console.log(undefinedVar);  // Output: undefined
console.log(nullVar);       // Output: null
console.log(objectVar);     // Output: { key: 'value' }
console.log(arrayVar);      // Output: [1, 2, 3]
functionVar();              // Output: 'Hello from a function!'
console.log(symbolVar);     // Output: Symbol(unique)
